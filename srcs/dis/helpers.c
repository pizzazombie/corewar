void	ft_exit(char *line)
{
	ft_printf(ANSI_COLOR_RED "%s" ANSI_COLOR_RESET "\n", line);
	exit(1);
}

void	ft_usage(void)
{
	ft_printf("usage:\n\t$>./dis_asm [flag] [file.cor]");
	ft_printf("\n\nuse -p flag to print result only to the standart output");
	ft_printf("\nuse -f flag to print disassembling detalils\n");
	ft_exit("");
}

void	ft_putstr_fd_wide(char c, char *str, int fd)
{
	ft_putchar_fd(c, fd);
	ft_putstr_fd(str, fd);
}

void	ft_print_s(t_all *all)
{
	char *str;

	while (get_next_line(all->fd, &str) > 0)
		ft_printf("%s\n", str);
}
